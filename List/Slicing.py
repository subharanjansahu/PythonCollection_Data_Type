list = ['p','y','t','h','o','n']
# disply 2nd element to 5th element
print(list[1:5])    # ['y', 't', 'h', 'o']
# Change items
list[1:5] = ['s','a','h','u']
print(list)     # ['p', 's', 'a', 'h', 'u', 'n']

# Access -ve index
print(list[-5:-1])
print(list[-5:])
print(list[:-2])
# print all element
print(list[:])
print(list[0:])
print(list[:6])
print(list[-6:])
print(list[-6:3])